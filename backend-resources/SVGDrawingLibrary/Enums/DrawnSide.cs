

namespace SVGDrawingLibrary.Enums
{
    /// <summary>
    /// The Side being Drawn
    /// </summary>
    public enum DrawnSide
    {
        /// <summary>
        /// The Front View of the Draw
        /// </summary>
        Front = 0,

        /// <summary>
        /// The Back View of the Draw
        /// </summary>
        Rear = 1,

        /// <summary>
        /// The Left or Right View of the Draw (When either of them is the same)
        /// </summary>
        Side = 2,

        /// <summary>
        /// The Left View of the Draw
        /// </summary>
        LeftSide = 3,

        /// <summary>
        /// The Right View of the Draw
        /// </summary>
        RightSide = 4,

        /// <summary>
        /// The Top View of the Draw
        /// </summary>
        Top = 5
    }
}

